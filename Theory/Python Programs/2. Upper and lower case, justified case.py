# length
#left and rigth justified
# upper and lower case
s=input("Enter String: ")
print("Length of string: ",len(s))
print()
print("Input Left Justified !")
print(s.ljust(50))
print()
print("Input Right Justified !")
print(s.rjust(50))
print()
print("Input in Lower Case !")
print(s.lower())
print()
print("Input in Upper Case !")
print(s.upper())
